export function getRandomFortune(type: FortuneType): string {
  const pool =
    type === 'love' ? LOVE_FORTUNES :
    type === 'career' ? CAREER_FORTUNES :
    HEALTH_FORTUNES;
  return pool[Math.floor(Math.random() * pool.length)];
}
