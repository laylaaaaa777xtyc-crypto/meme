export function generateLightMessage(wishText: string): string {
  for (const { keywords, messages } of TEMPLATES) {
    if (keywords.some(k => wishText.includes(k))) {
      return messages[Math.floor(Math.random() * messages.length)];
    }
  }
  return FALLBACKS[Math.floor(Math.random() * FALLBACKS.length)];
}
